// Global helper
static void SSD1306_SendCommand(uint8_t cmd) {
    uint8_t d[2] = {0x00, cmd};
    HAL_I2C_Master_Transmit(&hi2c1, SSD1306_I2C_ADDR, d, 2, HAL_MAX_DELAY);
}

static void SSD1306_SendData(uint8_t *data, uint16_t size) {
    uint8_t buffer[129];
    buffer[0] = 0x40; 
    memcpy(&buffer[1], data, size);
    HAL_I2C_Master_Transmit(&hi2c1, SSD1306_I2C_ADDR, buffer, size + 1, HAL_MAX_DELAY);
}

