void SSD1306_Clear(void) {
    uint8_t zero[128];
    memset(zero, 0x00, sizeof(zero));
    for (uint8_t page = 0; page < 8; page++) {
        SSD1306_SendCommand(0xB0 + page);
        SSD1306_SendCommand(0x00);
        SSD1306_SendCommand(0x10);
        SSD1306_SendData(zero, 128);
    }
}

void SSD1306_AllOn(void) {
    uint8_t full[128];
    memset(full, 0xFF, sizeof(full));
    for (uint8_t page = 0; page < 8; page++) {
        SSD1306_SendCommand(0xB0 + page);
        SSD1306_SendCommand(0x00);
        SSD1306_SendCommand(0x10);
        SSD1306_SendData(full, 128);
    }
}

void SSD1306_SetCursor(uint8_t x, uint8_t y) {
    SSD1306_SendCommand(0xB0 + y);
    SSD1306_SendCommand(0x00 + (x & 0x0F));
    SSD1306_SendCommand(0x10 + ((x >> 4) & 0x0F));
}

// Write one character
void SSD1306_WriteChar(char c) {
    if (c < 32 || c > 126) c = '?';
    const uint8_t *bitmap = Font5x7[c - 32];
    SSD1306_SendData((uint8_t *)bitmap, 5);
    uint8_t space = 0x00;
    SSD1306_SendData(&space, 1);
}

// Write string
void SSD1306_WriteString(char *str) {
    while (*str) {
        SSD1306_WriteChar(*str++);
    }
}

void SSD1306_DisplayOn(void) { SSD1306_SendCommand(0xAF); }
void SSD1306_DisplayOff(void){ SSD1306_SendCommand(0xAE); }
